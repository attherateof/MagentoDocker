#!/usr/bin/env bash
set -Eeuo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PHP_BIN_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
PROJECT_ROOT="$(cd "$PHP_BIN_DIR/../.." && pwd)"

source "$SCRIPT_DIR/common.sh"

ENV_FILE="$PROJECT_ROOT/.env"

set -a
source "$ENV_FILE"
set +a

required_vars=(
    PHP_FPM_CONTAINER
    NGINX_CONTAINER
    MARIADB_CONTAINER
    PRIMARY_OPENSEARCH_CONTAINER
    MYSQL_DATABASE
    MYSQL_USER
    MYSQL_PASSWORD
    COMPOSER_PUBLIC_KEY
    COMPOSER_PRIVATE_KEY
    MAGENTO_BASE_URL
    MAGENTO_ADMIN_FIRSTNAME
    MAGENTO_ADMIN_LASTNAME
    MAGENTO_ADMIN_EMAIL
    MAGENTO_ADMIN_USER
    MAGENTO_ADMIN_PASSWORD
    MAGENTO_LANGUAGE
    MAGENTO_CURRENCY
    MAGENTO_TIMEZONE
    VALKEY_SESION_CONTAINER
    CONTAINER_VALKEY_SESSION_PORT
    VALKEY_CACHE_CONTAINER
    CONTAINER_VALKEY_CACHE_PORT
    RABBIT_MQ_CONTAINER
    CONTAINER_RABBIT_MQ_MESSAGING_PORT
    HOST_MACHINE_RABBIT_MQ_WEB_UI_PORT
    HOST_MACHINE_OPENSEARCH_DASHBOARD_PORT
    HOST_MACHINE_PHP_MY_ADMIN_WEB_UI_PORT
    HOST_MACHINE_REDIS_INSIGHT_PORT
    HOST_MACHINE_MAILPIT_WEB_UI_PORT
    CONTAINER_MAILPIT_SMTP_PORT
    HOST_MACHINE_JENKINS_WEB_UI_PORT
    VARNISH_CONTAINER
    CONTAINER_VARNISH_PORT
    CONTAINER_NGINX_HTTP_PORT
)

for var in "${required_vars[@]}"; do
    [[ -n "${!var:-}" ]] || fail "$var is missing"
done

[[ "$COMPOSER_PUBLIC_KEY" != *xxxx* ]] \
    || fail "COMPOSER_PUBLIC_KEY contains placeholder value"

[[ "$COMPOSER_PRIVATE_KEY" != *xxxx* ]] \
    || fail "COMPOSER_PRIVATE_KEY contains placeholder value"

ok "Environment validation successful"
